const tmp = mkdtempSync(join(tmpdir(), "clawrouter-icons-"));

try {
  const packages = {
    lobe: unpackPackage(lobePackage, tmp),
  };

  const icons = {};
  for (const [id, config] of Object.entries(providers)) {
    if (config.package === "lobe") {
      icons[id] = fromSvgFile(join(packages.lobe.root, config.file), {
        label: config.label,
        source: `${packages.lobe.name}@${packages.lobe.version}/${config.file}`,
      });
      continue;
    }
    if (config.package === "url") {
      icons[id] = fromSvg(
        execFileSync("curl", ["--fail", "--silent", "--show-error", "--location", config.url], {
          encoding: "utf8",
        }),
        {
          label: config.label,
          source: config.url,
          viewBox: config.viewBox,
          pathIndex: config.pathIndex,
        },
      );
      continue;
    }

    throw new Error(`unsupported icon package ${config.package} for ${id}`);
  }

  writeFileSync(
    output,
    `${JSON.stringify(
      {
        note: "Generated by scripts/sync-provider-icons.mjs from static vendor icon sources.",
        icons,
      },
      null,
      2,
    )}\n`,
  );
  console.log(`wrote ${output.pathname}`);
} finally {
  rmSync(tmp, { recursive: true, force: true });
}

function unpackPackage(spec, dir) {
  const tarball = execFileSync("npm", ["pack", spec, "--silent", "--pack-destination", dir], {
    encoding: "utf8",
  })
    .trim()
    .split(/\n/)
    .at(-1);
  if (!tarball) {
    throw new Error(`npm pack produced no tarball for ${spec}`);
  }
  const packageDir = join(dir, spec.replace(/[^a-z0-9]+/gi, "-"));
  execFileSync("mkdir", ["-p", packageDir]);
  execFileSync("tar", ["-xzf", join(dir, tarball), "-C", packageDir]);
  const root = join(packageDir, "package");
  const metadata = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));
  return { root, name: metadata.name, version: metadata.version };
}

function fromSvgFile(file, metadata) {
  return fromSvg(readFileSync(file, "utf8"), metadata);
}

function fromSvg(svg, { viewBox: viewBoxOverride, pathIndex, ...metadata }) {
  const viewBox = viewBoxOverride ?? match(svg, /viewBox="([^"]+)"/);
  const body = svg
    .replace(/^.*?<svg[^>]*>/s, "")
    .replace(/<\/svg>\s*$/s, "")
    .replace(/<title>.*?<\/title>/gs, "")
    .trim();
  const paths = body.match(/<path\b[^>]*><\/path>|<path\b[^>]*\/>/g) ?? [];
  return { ...metadata, viewBox, body: pathIndex === undefined ? body : paths.at(pathIndex) ?? body };
}

function match(value, pattern) {
  const result = value.match(pattern);
  if (!result) {
    throw new Error(`pattern not found: ${pattern}`);
  }
  return result[1];
}
